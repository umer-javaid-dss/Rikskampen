package com.kampen.riks.app.rikskampen.leader.activity.fragments.home.addactivity.model;

public class DailyPick {

    private  String  id;
    private  String  path;
    private  int     tempRecourse;
    private  String  time;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public int getTempRecourse() {
        return tempRecourse;
    }

    public void setTempRecourse(int tempRecourse) {
        this.tempRecourse = tempRecourse;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
